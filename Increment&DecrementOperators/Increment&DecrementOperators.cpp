#include <iostream>
using namespace std;

int main()
{
    int A = 10, B = 20;

    cout << "A = " << A << endl;
    cout << "B = " << B << endl;

    A++; // you can use ++A as well
    B--; // you can use --B as well

    cout << "A = " << A << endl;
    cout << "B = " << B << endl;

    return 0;
}
